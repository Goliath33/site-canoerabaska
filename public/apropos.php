  <div class="box"><h1>Hélene & Jean Claude</h1>
	  Nous habitons la ferme de l'Ilot depuis 1978. Cette propriété agricole est dans la famille d'Hélène depuis plusieurs générations.
Il y a 20 ans j'ai repris l'activité canoë qui m'a amené à créer l'association Canoe Rabaska et puis la structure Balade au Fil de l'Eau.

Depuis toujours j'ai eu la passion de l'eau se qui m'a poussé dés l'âge de 15 ans à découvrir le canoë au sein du club des jeunes de mon village : Pessac sur Dordogne. Puis j'ai  fait de la compétition de descente de rivière en canoe monoplace durant de nonbreuses années. J'ai d'abord était Moniteur Fédéral de canoë kayak puis plus tard j'ai complété ma formation en devenant Educateur Sportif CK  avec une spécialisation handisport.

En 2007 avec l'Asso Canoe Rabaska nous avons construit le premier Rabaska ,en 2012 le Rabaska 2 et en 2014 le Rabaska 3 se qui me permet d'accueillir jusqu'à 45 personnes.

Aujourd'hui mon plus grand plaisir est de faire découvrir au plus prés de la nature notre belle rivière avec son histoire et sa richesse naturelle.</div>
  
